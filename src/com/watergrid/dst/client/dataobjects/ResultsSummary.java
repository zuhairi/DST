//this class represents a summary of each results available 
//this is for ease of use of the DataGrid
// the model manager creates and ArrayList of this objects to pass the view a summary of all the available results

package com.watergrid.dst.client.dataobjects;

public class ResultsSummary {
	
	String scenarioName;
	String simulationType;
	int scenarioIndex;
	String resultsTime;
	
	public ResultsSummary(String scenarioName,String simulationType,int scenarioIndex,String resultsTime){
		this.scenarioName=scenarioName;
		this.simulationType=simulationType;
		this.scenarioIndex=scenarioIndex;
		this.resultsTime=resultsTime;
	}

	public String getScenarioName() {
		return scenarioName;
	}

	public String getSimulationType() {
		return simulationType;
	}

	public int getScenarioIndex() {
		return scenarioIndex;
	}
	
	public String getResultsTime() {
		return resultsTime;
	}

}
