//object instances of these classes represent the summary results of a SINGLE component (link or node) for a specific type of simulation.

package com.watergrid.dst.client.dataobjects;

public class NodeChemicalResult {
	
	String nodeId;
	float minChemical,maxChemical, avgChemical;
	
	
	public NodeChemicalResult(String nodeId, float minChemical,float maxChemical, float avgChemical) {
		this.nodeId = nodeId;
		this.minChemical = minChemical;
		this.maxChemical = maxChemical;
		this.avgChemical = avgChemical;
	}


	public String getNodeId() {
		return nodeId;
	}


	public float getMinChemical() {
		return minChemical;
	}


	public float getMaxChemical() {
		return maxChemical;
	}


	public float getAvgChemical() {
		return avgChemical;
	}
	
	
	
	

}
